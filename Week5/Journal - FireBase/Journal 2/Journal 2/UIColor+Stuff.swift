import Foundation
import UIKit

extension UIColor {
    
    static func cyan() -> UIColor {
        return UIColor.cyanColor()
    }
    
    static func darkGray() -> UIColor {
        return UIColor.darkGrayColor()
    }
    
    static func lightGray() -> UIColor {
        return UIColor.lightGrayColor()
    }
}