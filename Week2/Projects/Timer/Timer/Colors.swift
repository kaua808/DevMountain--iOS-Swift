import Foundation
import UIKit

extension UIColor {
    
    static func orangeColorTimer() -> UIColor {
        return UIColor(red: 252 / CGFloat(255), green: 126 / CGFloat(255), blue: 15 / CGFloat(255), alpha: 1.0)
    }
    
    static func brownColorTimer() -> UIColor {
        return UIColor(red: 229 / CGFloat(255), green: 133 / CGFloat(255), blue: 47 / CGFloat(255), alpha: 1.0)
    }
    
    static func tanColorTimer() -> UIColor {
        return UIColor(red: 240 / CGFloat(255), green: 167 / CGFloat(255), blue: 104 / CGFloat(255), alpha: 1.0)
    }
    
    static func blueColorTimer() -> UIColor {
        return UIColor(red: 0 / CGFloat(255), green: 144 / CGFloat(255), blue: 175 / CGFloat(255), alpha: 1.0)
    }
    
    static func lightBlueColorTimer() -> UIColor {
        return UIColor(red: 15 / CGFloat(255), green: 210 / CGFloat(255), blue: 252 / CGFloat(255), alpha: 1.0)
    }
}