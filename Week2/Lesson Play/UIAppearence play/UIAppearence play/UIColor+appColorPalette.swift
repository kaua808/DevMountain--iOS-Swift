import Foundation
import UIKit

extension UIColor {
    
    static func sunsetRedColor() -> UIColor {
        return UIColor(red: 0.796, green: 0.239, blue: 0.212, alpha: 1.00)
    }
    
    static func sunsetOrange() -> UIColor {
        return UIColor(red: 198 / 255.0, green: 131/255.0, blue: 69/255.0, alpha: 1)
    }
    
    static func sunsetYellow() -> UIColor {
        return UIColor(red: 0.784, green: 0.643, blue: 0.267, alpha: 1.00)
    }
}