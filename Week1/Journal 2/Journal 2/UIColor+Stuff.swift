import Foundation
import UIKit

extension UIColor {
    
    static func blue() -> UIColor {
        return UIColor.magenta
    }
    
    static func darkGray() -> UIColor {
        return UIColor.darkGray
    }
    
    static func lightGray() -> UIColor {
        return UIColor.lightGray
    }
}
